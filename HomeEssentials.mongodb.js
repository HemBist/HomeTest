db.HomeEssentials.find({Id:1},{"Kitchen":1,_id:0})



//Find the details of Name Elements Pots
db.HomeEssentials.aggregate([
  { $match: { Id: 1 } },
  {
    $project: {
      Kitchen: {
        $filter: {
          input: {
            $map: {
              input:"$Kitchen",
              as: "kitchen",
              in: {
                Cookware: {
                  $cond: {
                    if: {$isArray:"$$kitchen.Cookware" },
                    then: {
                      $filter: {
                        input: "$$kitchen.Cookware",
                        as: "item",
                        cond: {
                          $and: [
                            { $ne: ["$$item", null] },
                            { $eq: ["$$item.Name", "Pots"] },
                          ]
                        }
                      }
                    },
                    else: []
                  }
                }
              }
            }
          },
          as: "kitchen",
          cond: { $gt: [{ $size: "$$kitchen.Cookware" }, 0] }
        }
      }
    }
  }
])

